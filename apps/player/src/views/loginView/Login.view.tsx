"use client";

import { useState } from "react";
import Link from "next/link";
import Image from "next/image";
import styles from "./Login.module.scss";
import { useLogin } from "@/lib/auth-hooks";
import { getLoginErrorMessage, isValidEmail } from "./functions";
import { Button, Card, CardBody, CardHeader, TextField } from "@tapestry/ui";

export default function LoginView() {
  const login = useLogin();

  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");

  const isBusy = login.isPending;

  const emailOk = isValidEmail(email);
  const canSubmit = emailOk && password.length >= 1 && !isBusy;

  const onSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    if (!canSubmit) return;

    login.mutate({ email, password });
  };

  return (
    <div className={styles.wrap}>
      <Card className={styles.card} padding="lg">
        <CardHeader>
          <div className={styles.logoContainer}>
            <Image
              src={
                "https://res.cloudinary.com/dmc7wmarf/image/upload/v1771775270/ChatGPT_Image_Jan_10_2026_11_32_39_AM_-_Copy_bcpc4f.png"
              }
              alt="Tapestry Logo"
              width={360}
              height={360}
              className={styles.logo}
            />
          </div>
          <h1 className={styles.title}>Welcome back</h1>
        </CardHeader>
        <CardBody>
          <form className={styles.form} onSubmit={onSubmit}>
            <div className={styles.field}>
              <TextField
                id="email"
                label="Email"
                floatingLabel
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                size={"md" as any}
              />
            </div>
            <div className={styles.field}>
              <TextField
                id="password"
                label="Password"
                type="password"
                floatingLabel
                value={password}
                onChange={(e) => setPassword(e.target.value)}
                showPasswordToggle
              />
            </div>
            {login.isError && (
              <div className={styles.errorBox} role="alert">
                {getLoginErrorMessage(login.error)}
              </div>
            )}
            <Button className={styles.primaryBtn} type="submit" disabled={!canSubmit}>
              {isBusy ? "Signing inâ€¦" : "Sign in"}
            </Button>
          </form>

          <div className={styles.links}>
            <hr className={styles.divider} />
            <Link href="/forgot-password" className={styles.link}>
              Forgot your password?
            </Link>
            <Link href="/register" className={styles.link}>
              New here? Create an account
            </Link>
          </div>
        </CardBody>
      </Card>
    </div>
  );
}
